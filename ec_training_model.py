class ACOConfigSearch:
    """
    Elitist ACO for model/hyperparameter configuration selection.
    Objective: J = emission_kg + lambda * (1 - accuracy) + carbon_penalty
    """

    def __init__(self, all_results, lambda_carbon=LAMBDA_CARBON,
                 n_ants=ACO_ANTS, n_iterations=ACO_ITERATIONS,
                 alpha=1.0, beta=2.0, rho=0.1, elitist_k=3,
                 carbon_budget=1.0):
        self.results       = all_results
        self.lambda_carbon = lambda_carbon
        self.n_ants        = n_ants
        self.n_iter        = n_iterations
        self.alpha         = alpha
        self.beta          = beta
        self.rho           = rho
        self.elitist_k     = elitist_k
        self.carbon_budget = carbon_budget

        self.model_names = [r["model"] for r in all_results]
        self.lr_options  = [1e-4, 5e-4, 1e-3, 5e-3]
        self.wd_options  = [1e-5, 1e-4, 1e-3]
        self.ep_options  = [50, 75, 100]

        n_m = len(self.model_names)
        n_l = len(self.lr_options)
        n_w = len(self.wd_options)
        n_e = len(self.ep_options)

        self.tau_model = np.ones(n_m)
        self.tau_lr    = np.ones(n_l)
        self.tau_wd    = np.ones(n_w)
        self.tau_ep    = np.ones(n_e)

        gs = np.array([r.get("green_score", 1.0) for r in all_results])
        gs = np.where(gs == 0, 1e-6, gs)
        self.eta_model = gs / gs.sum()
        self.eta_lr    = np.array([1/v for v in self.lr_options]); self.eta_lr /= self.eta_lr.sum()
        self.eta_wd    = np.array([1/v for v in self.wd_options]); self.eta_wd /= self.eta_wd.sum()
        self.eta_ep    = np.array(self.ep_options, dtype=float);   self.eta_ep /= self.eta_ep.sum()

        self.history      = []
        self.best_config  = None
        self.best_fitness = float("inf")

    def _select(self, tau, eta):
        weights = (tau ** self.alpha) * (eta ** self.beta)
        weights = np.maximum(weights, 1e-10)
        probs   = weights / weights.sum()
        return int(np.random.choice(len(tau), p=probs))

    def _fitness(self, config):
        r        = self.results[config["model_idx"]]
        emission = r.get("total_emission_kg", 1.0)
        accuracy = r.get("test_acc1", 0.0)
        penalty  = 10.0 if emission > self.carbon_budget else 0.0
        return emission + self.lambda_carbon * (1 - accuracy) + penalty

    def _adaptive_evaporation(self, iteration):
        return self.rho + 0.3 * (iteration / self.n_iter)

    def run(self):
        print(f"\n{'='*60}")
        print("[ACO] Starting Elitist Ant Colony Optimization")
        print(f"      Ants: {self.n_ants} | Iterations: {self.n_iter} | λ: {self.lambda_carbon}")
        print(f"{'='*60}")

        _gpu_ids = [] if DEVICE_TYPE == "cpu" else None
        aco_tracker = EmissionsTracker(
            project_name="aco_optimization",
            output_dir=ARTIFACT_DIR,
            output_file="aco_emissions.csv",
            log_level="error",
            save_to_file=True,
            gpu_ids=_gpu_ids,
        )
        aco_tracker.start()

        for it in range(1, self.n_iter + 1):
            ant_solutions = []
            for _ in range(self.n_ants):
                config = {
                    "model_idx": self._select(self.tau_model, self.eta_model),
                    "lr_idx":    self._select(self.tau_lr,    self.eta_lr),
                    "wd_idx":    self._select(self.tau_wd,    self.eta_wd),
                    "ep_idx":    self._select(self.tau_ep,    self.eta_ep),
                }
                config["fitness"] = self._fitness(config)
                ant_solutions.append(config)

            ant_solutions.sort(key=lambda x: x["fitness"])
            elite = ant_solutions[:self.elitist_k]

            if ant_solutions[0]["fitness"] < self.best_fitness:
                self.best_fitness = ant_solutions[0]["fitness"]
                self.best_config  = deepcopy(ant_solutions[0])

            rho_t = self._adaptive_evaporation(it)
            self.tau_model *= (1 - rho_t)
            self.tau_lr    *= (1 - rho_t)
            self.tau_wd    *= (1 - rho_t)
            self.tau_ep    *= (1 - rho_t)

            for sol in elite:
                deposit = 1.0 / max(sol["fitness"], 1e-9)
                self.tau_model[sol["model_idx"]] += deposit
                self.tau_lr   [sol["lr_idx"]   ] += deposit
                self.tau_wd   [sol["wd_idx"]   ] += deposit
                self.tau_ep   [sol["ep_idx"]   ] += deposit

            for tau in [self.tau_model, self.tau_lr, self.tau_wd, self.tau_ep]:
                np.clip(tau, 0.01, 10.0, out=tau)

            best_model_name = self.model_names[self.best_config["model_idx"]]
            self.history.append({
                "iteration":    it,
                "best_fitness": round(self.best_fitness, 6),
                "best_model":   best_model_name,
                "rho_adaptive": round(rho_t, 4),
                "tau_model":    self.tau_model.tolist(),
            })

            if it % 5 == 0 or it == 1:
                print(f"  Iter {it:3d}/{self.n_iter} | Best Fitness: {self.best_fitness:.6f} | "
                      f"Best Model: {best_model_name} | ρ: {rho_t:.4f}")

        aco_emission = aco_tracker.stop() or 0.0
        print(f"\n[ACO] Done. Best fitness: {self.best_fitness:.6f} | "
              f"Best model: {self.model_names[self.best_config['model_idx']]}")
        print(f"[ACO] Emission during optimization: {aco_emission:.6f} kg")

        resolved = {
            "model":           self.model_names[self.best_config["model_idx"]],
            "lr":              self.lr_options [self.best_config["lr_idx"]],
            "weight_decay":    self.wd_options [self.best_config["wd_idx"]],
            "epochs":          self.ep_options [self.best_config["ep_idx"]],
            "fitness":         self.best_fitness,
            "aco_emission_kg": round(aco_emission, 8),
        }

        return resolved, self.history, {
            "tau_model": self.tau_model.tolist(),
            "tau_lr":    self.tau_lr.tolist(),
            "tau_wd":    self.tau_wd.tolist(),
            "tau_ep":    self.tau_ep.tolist(),
        }

