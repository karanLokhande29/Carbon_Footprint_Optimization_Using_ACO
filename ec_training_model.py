def build_leaderboard(all_results):
    ranked = sorted(deepcopy(all_results), key=lambda x: x.get("test_acc1", 0), reverse=True)
    for i, r in enumerate(ranked):
        r["rank"] = i + 1
    return ranked

def print_leaderboard(leaderboard):
    table = PrettyTable()
    table.field_names = ["Rank", "Model", "Type", "Acc@1", "Acc@5", "F1",
                         "Emission(kg)", "GreenScore", "Size(MB)"]
    for r in leaderboard:
        table.add_row([
            r.get("rank", "-"),
            r["model"],
            r["type"],
            f"{r.get('test_acc1', 0):.4f}",
            f"{r.get('test_acc5', 0) or 0:.4f}",
            f"{r.get('f1_macro', 0):.4f}",
            f"{r.get('total_emission_kg', 0):.6f}",
            f"{r.get('green_score', 0):.4f}",
            f"{r.get('model_size_mb', 0):.2f}",
        ])
    print("\n[LEADERBOARD]")
    print(table)

