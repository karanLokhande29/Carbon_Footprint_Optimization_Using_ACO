def consolidate_emissions_csv():
    frames = []
    for csv_file in Path(ARTIFACT_DIR).glob("*.csv"):
        # KNN has been removed from the pipeline — skip its stale emission log
        if csv_file.name.startswith("classical_knn"):
            print(f"[INFO] Skipping removed-model emission log: {csv_file.name}")
            continue
        try:
            df = pd.read_csv(csv_file)
            df["source_file"] = csv_file.name
            frames.append(df)
        except Exception:
            pass
    if frames:
        master = pd.concat(frames, ignore_index=True)
        master.to_csv(f"{ARTIFACT_DIR}/emissions.csv", index=False)
        print(f"[INFO] Consolidated emissions CSV: {len(master)} rows")
    else:
        pd.DataFrame().to_csv(f"{ARTIFACT_DIR}/emissions.csv", index=False)


def save_all_artifacts(leaderboard, aco_best, aco_history, aco_pheromones, all_training_curves):
    with open(f"{ARTIFACT_DIR}/leaderboard.json", "w") as f:
        json.dump(leaderboard, f, indent=2)
    with open(f"{ARTIFACT_DIR}/aco_logs.json", "w") as f:
        json.dump({"best_config": aco_best, "history": aco_history,
                   "pheromones": aco_pheromones}, f, indent=2)
    with open(f"{ARTIFACT_DIR}/training_curves.json", "w") as f:
        json.dump(all_training_curves, f, indent=2)
    print(f"[INFO] All JSON artifacts saved to {ARTIFACT_DIR}/")

def zip_artifacts():
    zip_path = "./carbon_aco_artifacts.zip"
    with zipfile.ZipFile(zip_path, "w", zipfile.ZIP_DEFLATED) as zf:
        for fpath in Path(ARTIFACT_DIR).rglob("*"):
            if fpath.is_file():
                zf.write(fpath, arcname=fpath.relative_to(ARTIFACT_DIR))
    size_mb = os.path.getsize(zip_path) / 1e6
    print(f"[INFO] Artifacts zipped: {zip_path} ({size_mb:.2f} MB)")
    return zip_path

