def get_optimizer_scheduler(model, train_steps, lr=1e-3, wd=1e-4):
    optimizer = optim.AdamW(model.parameters(), lr=lr, weight_decay=wd)
    scheduler = optim.lr_scheduler.OneCycleLR(
        optimizer,
        max_lr=lr,
        total_steps=train_steps,
        pct_start=0.1,
        anneal_strategy="cos",
    )
    return optimizer, scheduler

def label_smooth_loss(pred, y_a, y_b, lam, smoothing=0.1):
    criterion = nn.CrossEntropyLoss(label_smoothing=smoothing)
    return lam * criterion(pred, y_a) + (1 - lam) * criterion(pred, y_b)

@torch.no_grad()
def evaluate(model, loader, use_resize=False):
    model.eval()
    correct1 = correct5 = total = 0
    latencies = []
    for images, labels in loader:
        if use_resize:
            images = F.interpolate(images, size=224, mode="bilinear", align_corners=False)
        images, labels = images.to(DEVICE), labels.to(DEVICE)
        t0  = time.perf_counter()
        out = model(images)
        latencies.append((time.perf_counter() - t0) / images.size(0) * 1000)
        _, pred5 = out.topk(5, dim=1)
        correct5 += pred5.eq(labels.view(-1, 1).expand_as(pred5)).any(dim=1).sum().item()
        correct1 += pred5[:, 0].eq(labels).sum().item()
        total    += labels.size(0)
    return correct1 / total, correct5 / total, float(np.mean(latencies))

@torch.no_grad()
def per_class_accuracy(model, loader, use_resize=False):
    model.eval()
    correct = np.zeros(NUM_CLASSES)
    counts  = np.zeros(NUM_CLASSES)
    for images, labels in loader:
        if use_resize:
            images = F.interpolate(images, size=224, mode="bilinear", align_corners=False)
        images = images.to(DEVICE)
        out    = model(images)
        preds  = out.argmax(dim=1).cpu().numpy()
        labs   = labels.numpy()
        for p, l in zip(preds, labs):
            counts[l] += 1
            if p == l:
                correct[l] += 1
    return (correct / np.maximum(counts, 1)).tolist()

def model_size_mb(model):
    tmp = "/tmp/tmp_model.pt"
    torch.save(model.state_dict(), tmp)
    size = os.path.getsize(tmp) / 1e6
    os.remove(tmp)
    return round(size, 2)

def get_all_preds_labels(model, loader, use_resize=False):
    model.eval()
    all_preds, all_labels = [], []
    with torch.no_grad():
        for images, labels in loader:
            if use_resize:
                images = F.interpolate(images, size=224, mode="bilinear", align_corners=False)
            out   = model(images.to(DEVICE))
            preds = out.argmax(dim=1).cpu().numpy()
            all_preds.extend(preds.tolist())
            all_labels.extend(labels.numpy().tolist())
    return np.array(all_preds), np.array(all_labels)

