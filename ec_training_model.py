def extract_embeddings(model, loader, use_resize=False):
    model.eval()
    embeddings, all_labels = [], []
    with torch.no_grad():
        for images, labels in loader:
            if use_resize:
                images = F.interpolate(images, size=224, mode="bilinear", align_corners=False)
            images = images.to(DEVICE)
            feats  = model.forward_features(images)
            if feats.dim() == 4:
                feats = feats.mean(dim=[2, 3])
            elif feats.dim() == 3:
                feats = feats[:, 0]   # CLS token for ViT
            embeddings.append(feats.cpu().numpy())
            all_labels.extend(labels.numpy().tolist())
    return np.concatenate(embeddings, axis=0), np.array(all_labels)


