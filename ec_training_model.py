def train_dl_model(model_name, epochs=EPOCHS):
    print(f"\n{'='*60}")
    print(f"[TRAIN] {model_name.upper()} | Epochs: {epochs}")
    print(f"{'='*60}")

    is_vit   = "vit" in model_name
    model    = MODEL_REGISTRY[model_name]()
    n_params = sum(p.numel() for p in model.parameters()) / 1e6
    print(f"[INFO] Parameters: {n_params:.2f}M")

    steps_per_epoch      = len(train_loader)
    total_steps          = steps_per_epoch * epochs
    optimizer, scheduler = get_optimizer_scheduler(model, total_steps)
    scaler               = GradScaler(device=DEVICE_TYPE)

    training_curves   = {"train_loss": [], "val_acc1": [], "val_acc5": [], "epoch_emission_kg": []}
    best_val_acc      = 0.0
    best_state        = None
    ckpt_path         = f"{ARTIFACT_DIR}/{model_name}_best.pt"
    total_emission_kg = 0.0

    _gpu_ids = [] if DEVICE_TYPE == "cpu" else None
    outer_tracker = EmissionsTracker(
        project_name=f"{model_name}_total",
        output_dir=ARTIFACT_DIR,
        output_file=f"{model_name}_total_emissions.csv",
        log_level="error",
        save_to_file=True,
        gpu_ids=_gpu_ids,
    )
    outer_tracker.start()

    # Resume from checkpoint if session was interrupted
    start_epoch = 1
    for resume_ep in range((epochs // 10) * 10, 0, -10):
        resume_ckpt = f"{ARTIFACT_DIR}/{model_name}_epoch{resume_ep}.pt"
        if os.path.exists(resume_ckpt):
            print(f"[RESUME] Found checkpoint at epoch {resume_ep}: {resume_ckpt}")
            ckpt_data = torch.load(resume_ckpt, map_location=DEVICE, weights_only=True)
            model.load_state_dict(ckpt_data["model_state_dict"])
            try:
                optimizer.load_state_dict(ckpt_data["optimizer_state_dict"])
            except Exception:
                pass
            best_val_acc      = ckpt_data.get("best_val_acc", 0.0)
            total_emission_kg = ckpt_data.get("total_emission_kg", 0.0)
            start_epoch       = resume_ep + 1
            remaining = (epochs - resume_ep) * steps_per_epoch
            if remaining > 0:
                _, scheduler = get_optimizer_scheduler(model, remaining, lr=1e-3)
            print(f"[RESUME] Resuming from epoch {start_epoch}")
            break

    for epoch in range(start_epoch, epochs + 1):
        epoch_tracker = EmissionsTracker(
            project_name=f"{model_name}_e{epoch}",
            output_dir=ARTIFACT_DIR,
            output_file=f"{model_name}_e{epoch}_emissions.csv",
            log_level="error",
            save_to_file=True,
            gpu_ids=_gpu_ids,
        )
        epoch_tracker.start()

        model.train()
        epoch_loss = 0.0
        for images, labels in train_loader:
            images, labels = images.to(DEVICE), labels.to(DEVICE)
            if is_vit:
                images = F.interpolate(images, size=224, mode="bilinear", align_corners=False)
            images, y_a, y_b, lam = cutmix_batch(images, labels)
            optimizer.zero_grad(set_to_none=True)

            with autocast(device_type=DEVICE_TYPE):
                out  = model(images)
                loss = label_smooth_loss(out, y_a, y_b, lam)

            scaler.scale(loss).backward()
            scaler.unscale_(optimizer)
            nn.utils.clip_grad_norm_(model.parameters(), 1.0)
            scaler.step(optimizer)
            scaler.update()

            # Guard: OneCycleLR raises if stepped past total_steps
            if scheduler.last_epoch < scheduler.total_steps:
                scheduler.step()
            epoch_loss += loss.item()

        epoch_emission    = epoch_tracker.stop()
        epoch_emission_kg = epoch_emission if epoch_emission else 0.0
        total_emission_kg += epoch_emission_kg

        val_acc1, val_acc5, _ = evaluate(model, val_loader, use_resize=is_vit)
        avg_loss = epoch_loss / len(train_loader)

        training_curves["train_loss"].append(avg_loss)
        training_curves["val_acc1"].append(val_acc1)
        training_curves["val_acc5"].append(val_acc5)
        training_curves["epoch_emission_kg"].append(epoch_emission_kg)

        if val_acc1 > best_val_acc:
            best_val_acc = val_acc1
            best_state   = deepcopy(model.state_dict())
            torch.save(best_state, ckpt_path)

        # Periodic checkpoint every 10 epochs for crash-recovery
        if epoch % 10 == 0:
            torch.save({
                "epoch":                epoch,
                "model_state_dict":     model.state_dict(),
                "optimizer_state_dict": optimizer.state_dict(),
                "scheduler_state_dict": scheduler.state_dict(),
                "val_acc1":             val_acc1,
                "best_val_acc":         best_val_acc,
                "total_emission_kg":    total_emission_kg,
            }, f"{ARTIFACT_DIR}/{model_name}_epoch{epoch}.pt")

        if epoch % 5 == 0 or epoch == 1:
            print(f"  Epoch {epoch:3d}/{epochs} | Loss: {avg_loss:.4f} | "
                  f"Val Acc@1: {val_acc1:.4f} | Val Acc@5: {val_acc5:.4f} | "
                  f"Emission: {epoch_emission_kg:.6f} kg")

    total_emission = outer_tracker.stop()
    if total_emission:
        total_emission_kg = total_emission

    model.load_state_dict(torch.load(ckpt_path, map_location=DEVICE, weights_only=True))

    test_acc1, test_acc5, avg_lat = evaluate(model, test_loader, use_resize=is_vit)
    preds, labels_arr = get_all_preds_labels(model, test_loader, use_resize=is_vit)
    prec, rec, f1, _  = precision_recall_fscore_support(labels_arr, preds, average="macro", zero_division=0)
    per_cls_acc       = per_class_accuracy(model, test_loader, use_resize=is_vit)
    size_mb           = model_size_mb(model)

    super_acc = {}
    for sc_name, class_ids in SUPERCLASS_MAPPING.items():
        sc_idx   = np.isin(labels_arr, class_ids)
        sc_preds = preds[sc_idx]
        sc_labs  = labels_arr[sc_idx]
        super_acc[sc_name] = float(accuracy_score(sc_labs, sc_preds)) if len(sc_labs) > 0 else 0.0

    green_score = test_acc1 / max(total_emission_kg, 1e-9)

    result = {
        "model":                model_name,
        "type":                 "deep_learning",
        "test_acc1":            round(test_acc1, 4),
        "test_acc5":            round(test_acc5, 4),
        "val_acc1":             round(best_val_acc, 4),
        "precision_macro":      round(float(prec), 4),
        "recall_macro":         round(float(rec), 4),
        "f1_macro":             round(float(f1), 4),
        "inference_latency_ms": round(avg_lat, 4),
        "model_size_mb":        size_mb,
        "total_emission_kg":    round(total_emission_kg, 8),
        "green_score":          round(green_score, 4),
        "n_params_M":           round(n_params, 2),
        "checkpoint":           ckpt_path,
        "timestamp":            datetime.utcnow().isoformat(),
    }

    with open(f"{ARTIFACT_DIR}/{model_name}_training_curves.json", "w") as f:
        json.dump(training_curves, f, indent=2)
    with open(f"{ARTIFACT_DIR}/{model_name}_per_class.json", "w") as f:
        json.dump({"per_class_accuracy": per_cls_acc, "superclass_accuracy": super_acc}, f, indent=2)
    with open(f"{ARTIFACT_DIR}/records.jsonl", "a") as f:
        f.write(json.dumps(result) + "\n")

    print(f"\n[RESULT] {model_name} | Test@1: {test_acc1:.4f} | Test@5: {test_acc5:.4f} | "
          f"F1: {f1:.4f} | Emission: {total_emission_kg:.6f} kg | GreenScore: {green_score:.4f}")

    return model, result, training_curves

